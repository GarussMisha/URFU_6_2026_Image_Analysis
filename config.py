# Загрузка переменных окружения из .env файла
from dotenv import load_dotenv
import os

# Загружаем переменные из .env файла (если существует)
load_dotenv()

# ============================================
# НАСТРОЙКИ ПОДКЛЮЧЕНИЯ
# ============================================

# Параметры подключения к камере (читаются из .env)
# ВАЖНО: Все эти переменные должны быть указаны в .env файле!
# C001 - Имя пользователя для RTSP-камеры
RTSP_USERNAME = os.getenv("RTSP_USERNAME")
# C002 - Пароль для RTSP-камеры
RTSP_PASSWORD = os.getenv("RTSP_PASSWORD")
# C003 - IP-адрес RTSP-камеры
RTSP_IP = os.getenv("RTSP_IP")
# C004 - Порт RTSP-камеры
RTSP_PORT = os.getenv("RTSP_PORT")
# C005 - Номер потока RTSP-камеры
RTSP_STREAM = os.getenv("RTSP_STREAM")

# Проверка обязательных переменных
if not all([RTSP_USERNAME, RTSP_PASSWORD, RTSP_IP, RTSP_PORT, RTSP_STREAM]):
    raise EnvironmentError(
        "Необходимо заполнить параметры подключения в .env файле:\n"
        "RTSP_USERNAME, RTSP_PASSWORD, RTSP_IP, RTSP_PORT, RTSP_STREAM"
    )

# C006 - URL RTSP-камеры (собирается из отдельных параметров)
# Формат: rtsp://username:password@ip_address:port/stream_number
# stream1 - основной поток (высокое качество)
# stream2 - вторичный поток (низкое качество, больше FPS)
# Пример: rtsp://admin:password@192.168.0.100:554/stream2
RTSP_URL = f"rtsp://{RTSP_USERNAME}:{RTSP_PASSWORD}@{RTSP_IP}:{RTSP_PORT}/{RTSP_STREAM}"

# C007 - Директория для сохранения снимков при обнаружении движения
OUTPUT_DIR = "motion_snapshots"

# ============================================
# НАСТРОЙКИ ДЕТЕКЦИИ ДВИЖЕНИЯ
# ============================================

# D001 - MIN_AREA - минимальная площадь обнаруженного движения в пикселях
# Меньшие значения = более чувствительный детектор
# Увеличьте, если детектор реагирует на шум (листья, тени)
# Рекомендуемые значения:
#   - 5000-10000: для камер с высоким разрешением
#   - 3000-5000: для камер среднего разрешения
#   - 1000-3000: для камер с низким разрешением
MIN_AREA = 8000

# D002 - THRESHOLD - порог различия между кадрами (0-255)
# Меньшие значения = более чувствительный детектор
# Увеличьте при изменении освещения
# Рекомендуемые значения:
#   - 20-30: для стабильного освещения
#   - 30-50: для переменного освещения
#   - 50+: для сильно меняющегося освещения
THRESHOLD = 35

# D003 - MOTION_DURATION - минимальное количество кадров движения перед сохранением
# Помогает избежать ложных срабатываний от кратковременных помех
# При 25 FPS: 30 кадров = ~1.2 секунды
# Рекомендуемые значения:
#   - 15-20: быстрая реакция на движение
#   - 30-40: баланс между чувствительностью и ложными срабатываниями
#   - 50+: только значительное движение
MOTION_DURATION = 10

# D004 - COOLDOWN_SECONDS - время ожидания после сохранения снимка
# Предотвращает сохранение множества снимков подряд
# Рекомендуемые значения:
#   - 3-5: частые снимки при активном движении
#   - 10-15: умеренное сохранение
#   - 30+: редкие снимки только при значительном событии
COOLDOWN_SECONDS = 2

# D005 - FRAME_SKIP - пропуск кадров между анализами
# Уменьшает нагрузку на CPU
# При 30 FPS и FRAME_SKIP=2: анализ каждые 100 мс (~10 FPS)
# Рекомендуемые значения:
#   - 1: анализ каждого кадра (максимальная точность, высокая нагрузка)
#   - 2-3: баланс точности и производительности
#   - 4-5: низкая нагрузка, может пропустить быстрое движение
FRAME_SKIP = 2

# ============================================
# ВАЛИДАЦИЯ ПАРАМЕТРОВ
# ============================================

def validate_config():
    """Валидация параметров конфигурации"""
    errors = []
    
    # Валидация THRESHOLD (0-255)
    if not (0 <= THRESHOLD <= 255):
        errors.append(f"THRESHOLD должен быть в диапазоне 0-255, текущее значение: {THRESHOLD}")
    
    # Валидация FRAME_SKIP (>= 1)
    if FRAME_SKIP < 1:
        errors.append(f"FRAME_SKIP должен быть >= 1, текущее значение: {FRAME_SKIP}")
    
    # Валидация MIN_AREA (> 0)
    if MIN_AREA <= 0:
        errors.append(f"MIN_AREA должен быть > 0, текущее значение: {MIN_AREA}")
    
    # Валидация MOTION_DURATION (> 0)
    if MOTION_DURATION <= 0:
        errors.append(f"MOTION_DURATION должен быть > 0, текущее значение: {MOTION_DURATION}")
    
    # Валидация COOLDOWN_SECONDS (>= 0)
    if COOLDOWN_SECONDS < 0:
        errors.append(f"COOLDOWN_SECONDS должен быть >= 0, текущее значение: {COOLDOWN_SECONDS}")
    
    # Валидация RTSP_URL формата
    if not RTSP_URL.startswith("rtsp://"):
        errors.append(f"RTSP_URL должен начинаться с 'rtsp://', текущее значение: {RTSP_URL}")
    
    if errors:
        print("❌ Ошибки конфигурации:")
        for error in errors:
            print(f"   - {error}")
        raise ValueError("Неверные параметры конфигурации")
    
    print("✅ Конфигурация валидна")

# Запускаем валидацию при импорте
validate_config()
